/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Enable the Instruction Cache */
  CACHE_Enable();

  /* Initialize all configured peripherals */
  USART3_UART_Init();

  /* Configure the LCD ...*/
  BSP_Config();

  /* Call init function for freertos objects (in freertos.c) */
  osKernelInitialize();

  printf("HTTP Server Socket RTOS Application \r\n");
  printf("STM32H563ZI board \r\n");
  printf("State: Ethernet Initialization ... \r\n");

  /* Init thread */
  InitThreadHandle = osThreadNew(InitThread_Entry, NULL, &InitThread_attributes);

  /* Start scheduler */
  osKernelStart();

  /* We should never get here as control is now taken by the scheduler */
  /* Infinite loop */
  while (1)
  {}
}

/**
  * @brief  Init Thread
  * @param  argument not used
  * @retval None
  */
static void InitThread_Entry(void *argument)
{
  /* Create tcp_ip stack thread */
  tcpip_init(NULL, NULL);

  /* Initialize the LwIP stack */
  Netif_Config();

  /* Initialize webserver demo */
  http_server_socket_init();

  /* Delete the Init Thread */
  osThreadTerminate(InitThreadHandle);
}

